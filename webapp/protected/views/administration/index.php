<p>Le système d'administration vous permettra de gérer l'ensemble des objets présentés dans le système:</p><br>
<p>- <b>Gestion des utilisateurs</b>: <i>Affiche la liste des utilisateurs, avec un bouton de mise à jour et de suppression. Un lien est cliquable pour créer un nouvel utilisateur et un autre lien pour faire une recherche avancée.</i></p>
<p>- <b>Gestion des formulaires</b>: <i>Affiche la liste des formulaires, avec un bouton de mise à jour et de suppression. Un lien est cliquable pour créer un nouveau formulaire et un autre lien pour faire une recherche avancée.</i></p>
<p>- <b>Gestion des fiches</b>: <i>Affiche la liste des fiches, avec un bouton de mise à jour et de suppression. Un lien est cliquable pour créer une nouvelle fiche et un autre lien pour faire une recherche avancée.</i></p>
<p>- <b>Gestion des blocs</b>: <i>Affiche la liste des blocs, avec un bouton de mise à jour et de suppression. Chaque bloc contient une ou plusieurs questions. Un lien est cliquable pour créer un nouveau bloc et un autre lien pour faire une recherche avancée.</i></p>
<p>- <b>Gestion des droits</b>: <i>Affiche la liste des profils disponibles pour chaque type de fiche, avec un bouton de mise à jour des droits d'accès. Un lien est cliquable pour faire une recherche avancée.</i></p>
<p>- <b>Log système</b>: <i>Affiche la liste des traces des actions utilisateurs.</i></p>
